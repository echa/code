type AirportCode string

const (
	AirportCodeUndefined AirportCode = ""
)

func ParseAirportCode(c string) AirportCode {
	c = strings.ToUpper(c)
	for _, x := range IATA_LARGE_AIRPORT_CODES {
		if string(x) == c {
			return AirportCode(c)
		}
	}
	return AirportCodeUndefined
}

func (r AirportCode) IsValid() bool {
	return r != AirportCodeUndefined
}

// Text/JSON conversion
func (r AirportCode) MarshalText() ([]byte, error) {
	return []byte(r), nil
}

func (r *AirportCode) UnmarshalText(data []byte) error {
	rr := ParseAirportCode(string(data))
	if !rr.IsValid() {
		return fmt.Errorf("iata: invalid IATA airport code '%s'", string(data))
	}
	*r = rr
	return nil
}

// SQL conversion
func (r *AirportCode) Scan(value interface{}) error {
	switch v := value.(type) {
	case string:
		*r = ParseAirportCode(v)
	case []byte:
		*r = ParseAirportCode(string(v))
	}
	if !(*r).IsValid() {
		return fmt.Errorf("iata: invalid IATA airport code '%v'", value)
	}
	return nil
}

func (r AirportCode) Value() (driver.Value, error) {
	return string(r), nil
}

func (c AirportCode) String() string {
	return string(c)
}

func (r AirportCode) Airport() Airport {
	if a, ok := IATA_LARGE_AIRPORTS[r]; ok {
		return a
	}
	return Airport{}
}
